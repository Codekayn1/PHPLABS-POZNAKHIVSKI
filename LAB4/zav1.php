<?php
// Генерація масиву з 10 випадкових чисел від 1 до 100
$numbers = [];

for ($i = 0; $i < 10; $i++) {
    $numbers[] = rand(1, 100);
}

echo "Згенерований масив: " . implode(", ", $numbers) . "\n";

// Знаходження мінімального і максимального значення
$min = min($numbers);
$max = max($numbers);

echo "Мінімальне значення: $min\n";
echo "Максимальне значення: $max\n";
?>
